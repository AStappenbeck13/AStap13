package HorseBarnFRQ;

public class HorseFRQ {

    private String name;
    private int weight;

    public HorseFRQ (String n, int w)
    {
        name = n;
        weight = w;
    }

    public String getName()
    {
        return name;
    }

    public int getWeight()
    {
        return weight;
    }

}